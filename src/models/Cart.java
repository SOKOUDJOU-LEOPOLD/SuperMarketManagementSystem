package models;

/**
 *
 * @author leopo
 */
public class Cart {
    private int id;
    Cart(){
        
    }
    Cart(int id){
      this.id = id;  
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 71 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Cart other = (Cart) obj;
        return true;
    }
    
    
}
